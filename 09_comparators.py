'''
operadores
==, X  es igual que Y
!=, X es distinto de Y
<, X menor que Y
>, X mayor que Y
<=, X menor o igual que Y
>=, X menor o igual que Y

'''

print("Apple" == "Apple") # True, por que son iguales
print("Apple" == "apple") # False, por que son distintos
print("1" == 1) # False, por que son distintos tipo de datos. 'str', e 'int'
