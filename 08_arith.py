print( 10 + 10 ) # Suma
print( 10 - 5 )  # Resta
print( 10 * 2 )  # Multiplicación
print( 10 / 2 )  # División
print( 10 % 2 )  # Modulo
print( 10 ** 2 )  # Potencia
print( 10 // 2 )  # División intera, solo toma el numero entero no sus decimales



print( "Hola" + " mundo") # Concatenando
print( "Hola" * 3) # Repite x3 veces el texto.

